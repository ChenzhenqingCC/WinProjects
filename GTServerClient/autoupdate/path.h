struct Path 
{
	static CString GetAppPath()
	{
		TCHAR szFileName[MAX_PATH];
		::GetModuleFileName(_Module.GetModuleInstance(), szFileName, MAX_PATH);
		WTL::CString	 path=szFileName;
		int  dist = path.ReverseFind('\\');
		if(dist > -1)
		{
			path.Delete(dist,path.GetLength()-dist);
		}
		return path;
	}
};
